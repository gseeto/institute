<h3>Watch out for the Weaknesses of your 2nd Slice</h3>
<p>Unlike other leadership models, we do not believe you can avoid weaknesses, but you do need to know that on
a bad day you will exhibit the weaknesses of your secondary slice.</p>
<h3>Good Day - Bad Day</h3>
<p>Pressure or stress has a marked influence on our behavior. We behave, respond or react differently according to
our circumstances. At the same time we all have strengths and weakness. We exhibit our strengths and
weaknesses according to the circumstances that we find ourselves in.</p>
<p>You will need to read the book "LEMON Leadership" or attend the LEMON Leadership course to get a full
understanding of what this means, but put simply - on a Good Day, when there is not pressure and all is right
with the world, I live in the strengths of my Primary Slice. However, on a Bad Day, when the pressure is on, I
live in the weaknesses of my Secondary Slice.</p>
<p>Having bad days is normal, but living in the weaknesses of my Secondary slice is unhealthy. Understanding
LEMON can help you understand what is happening when you are having a bad day. It can also help you, and
others around you, constructively deal with the situation.</p>

