<h3>Introduction</h3>
<p>It is often stated that there is no shortage of capital or ideas, but that we have a drastic shortage
of leaders. The response to this has been a deluge of books and courses on how to lead.
However, we live in a culture that is structured around accumulating knowledge but ignoring
mechanisms to really find out who we are.</p>
<p>Peter Drucker has stated that of the thousands of students that he has worked with, many knew
what to do ... but very few knew who they were. Our belief is that it is more important in the long
run to know who you are - than to know what to do.
</p>
<p>LEMON Leadership is not a "How to" of leadership, but "Who are you?" It looks at leadership in
a fresh way.</p>
<p>If you are a good leader you will lead out of your identity. It doesn't matter too much what your
style, academic background or ethnicity may be. The key is to know what type of leader you
are. This assessment is based on Brett Johnson's book, LEMON Leadership, where he writes
about five types of leaders:</p>
