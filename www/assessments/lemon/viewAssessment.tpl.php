<h2>A brief look at LEMON Leadership</h2>
<p>There are 5 types of leaders, and all of us are a combination of the 5 types. When we do
coaching we look at these in detail, but here is the summary:</p>
<br>The <b>Luminary</b> lives in the world of Ideas and asks the question ... Why?</br>
<br>The <b>Entrepreneur</b> lives in the world of Opportunities and asks the question ... Why not?</br>
<br>The <b>Manager</b> lives in the world of Systems and asks the question ... How?</br>
<br>The <b>Organizer</b> lives in the world of Tasks and asks the question ... What?</br>
<br>The <b>Networker</b> lives in the world of Connections and asks the question ... Who?</br>
<h3>LEMON Leadership looks at ...</h3>
<ul>
	<li>How each LEMON defines reality</li>
	<li>Their starting point for everything that they do</li>
	<li>Who they gravitate towards, and who they ignore</li>
	<li>What each one defines as "Work"</li>
	<li>How they approach life, and everything that they do</li>
	<li>What they focus on</li>
	<li>Their depth of vision</li>
</ul>
<p>Once we have looked at each LEMON type in detail, we move on to see how they interact with
one another. We look at, discuss and workshop ...</p>
<ul>
	<li>Their strengths and weaknesses</li>
	<li>What they protect under pressure</li>
	<li>What they hold on to, and what they let go easily.</li>
	<li>How they communicate - what they mean when they all say the same thing and it means 5
different things</li>
	<li>How they handle confrontation and correction</li>
	<li>How they relate and behave in a team</li>
	<li>How they are on a good day, and how they are on a bad day</li>
	<li>What is important and unimportant to each one</li>
	<li>How to "twist the LEMON". i.e. - How to adapt to different circumstances
We get to understand what is on their radar</li>
</ul>
